#!/usr/bin/env node
/*
 * scripts/notify-stale-forks.mjs
 *
 * Open a one-click "sync your fork" PR against every fork of
 * sridhar-ravi-2917/Design-Token-Forge that is behind upstream main.
 *
 * Why: colleagues who forked early can't see new projects / get the
 * stale-fork redirect card until their fork redeploys. Asking each
 * person to click Sync fork + wait for the workflow is friction;
 * a PR they can merge in one click removes it.
 *
 * What this does NOT do:
 *   - Push to their fork (we don't have their PAT)
 *   - Force-merge anything (they still review + merge)
 *   - Touch private forks (the API only lists public forks)
 *
 * Usage:
 *   GITHUB_TOKEN=ghp_xxx node scripts/notify-stale-forks.mjs           # dry run
 *   GITHUB_TOKEN=ghp_xxx node scripts/notify-stale-forks.mjs --apply   # actually open PRs
 *
 * The PAT needs `public_repo` scope (read forks) + `repo` to open
 * cross-fork PRs.
 */

const OWNER = 'sridhar-ravi-2917';
const REPO  = 'Design-Token-Forge';
const BASE_BRANCH = 'main';

const TOKEN = process.env.GITHUB_TOKEN;
const APPLY = process.argv.includes('--apply');

if (!TOKEN) {
  console.error('GITHUB_TOKEN env var required (a PAT with public_repo + repo scopes).');
  process.exit(1);
}

const GH = 'https://api.github.com';

async function gh(path, init = {}) {
  const r = await fetch(GH + path, {
    ...init,
    headers: {
      Authorization: 'Bearer ' + TOKEN,
      Accept: 'application/vnd.github+json',
      'X-GitHub-Api-Version': '2022-11-28',
      ...(init.headers || {}),
    },
  });
  if (r.status === 404) return null;
  if (!r.ok) {
    const body = await r.text().catch(() => '');
    throw new Error(`${init.method || 'GET'} ${path} → ${r.status}: ${body.slice(0, 200)}`);
  }
  if (r.status === 204) return null;
  return r.json();
}

/* ── 1. Get current upstream SHA on main ──────────────────── */
const upstream = await gh(`/repos/${OWNER}/${REPO}/branches/${BASE_BRANCH}`);
const upstreamSha = upstream.commit.sha;
console.log(`Upstream ${OWNER}/${REPO}@${BASE_BRANCH} = ${upstreamSha.slice(0, 7)}`);

/* ── 2. List all forks (paginated) ────────────────────────── */
const forks = [];
let page = 1;
while (true) {
  const batch = await gh(`/repos/${OWNER}/${REPO}/forks?per_page=100&page=${page}`);
  if (!batch || !batch.length) break;
  forks.push(...batch);
  if (batch.length < 100) break;
  page++;
}
console.log(`Found ${forks.length} fork(s).`);

/* ── 3. For each fork, check freshness + open sync PR ─────── */
const PR_BRANCH = 'sync-from-upstream-' + upstreamSha.slice(0, 7);
const PR_TITLE  = '⬆ Sync fork with upstream (DTF v' + upstreamSha.slice(0, 7) + ')';
const PR_BODY = [
  'This PR brings your fork up to date with `' + OWNER + '/' + REPO + '@' + BASE_BRANCH + '`.',
  '',
  '**Why you\'re getting this:** the Design Token Forge auth gate refuses to',
  'redirect users to a fork that\'s out of date (the editor + project list',
  'shipped at the time of your fork have known bugs). Merging this PR will:',
  '',
  '1. Pull every commit you\'re behind on into your `main`.',
  '2. Trigger the `deploy-tokens` GitHub Actions workflow automatically.',
  '3. After ~2 min, `https://<your-username>.github.io/' + REPO + '/demo/` will',
  '   serve the current UI and your auth gate will work normally.',
  '',
  'You don\'t lose any of your own commits — this is a fast-forward merge.',
  'If you have local edits on `main`, GitHub will show a regular 3-way merge',
  'preview before you confirm.',
  '',
  '_Generated by `scripts/notify-stale-forks.mjs`._',
].join('\n');

const results = { fresh: 0, prOpened: 0, prExists: 0, error: 0, ahead: 0, skippedPrivate: 0 };

for (const fork of forks) {
  const owner = fork.owner.login;
  const tag   = owner + '/' + fork.name;

  /* Private forks can't be PR'd into from a cross-fork branch without
     more scopes; skip. */
  if (fork.private) {
    console.log(`  · ${tag} → private, skipping`);
    results.skippedPrivate++;
    continue;
  }

  try {
    /* Compare upstream main vs fork main. `status` is one of
       'identical', 'behind', 'ahead', 'diverged'. */
    const cmp = await gh(`/repos/${owner}/${fork.name}/compare/${BASE_BRANCH}...${OWNER}:${REPO}:${BASE_BRANCH}`);
    if (!cmp) { console.log(`  · ${tag} → 404 on compare, skipping`); results.error++; continue; }

    if (cmp.status === 'identical' || cmp.behind_by === 0) {
      console.log(`  ✓ ${tag} → up to date`);
      results.fresh++;
      continue;
    }
    if (cmp.status === 'ahead') {
      console.log(`  ↑ ${tag} → ahead of upstream by ${cmp.ahead_by}, skipping`);
      results.ahead++;
      continue;
    }

    console.log(`  ⤓ ${tag} → behind by ${cmp.behind_by} commit(s)` + (cmp.ahead_by ? `, ahead by ${cmp.ahead_by}` : ''));

    /* Already an open PR for this exact upstream SHA? Skip. */
    const existing = await gh(`/repos/${owner}/${fork.name}/pulls?state=open&head=${OWNER}:${BASE_BRANCH}`);
    if (existing && existing.length) {
      console.log(`    PR already open: #${existing[0].number} ${existing[0].html_url}`);
      results.prExists++;
      continue;
    }

    if (!APPLY) {
      console.log(`    [dry-run] would open PR: ${OWNER}:${BASE_BRANCH} → ${owner}:${BASE_BRANCH}`);
      continue;
    }

    /* Cross-fork PR: head = sridhar-ravi-2917:main, base = owner's main. */
    const pr = await gh(`/repos/${owner}/${fork.name}/pulls`, {
      method: 'POST',
      body: JSON.stringify({
        title: PR_TITLE,
        body:  PR_BODY,
        head:  OWNER + ':' + BASE_BRANCH,
        base:  BASE_BRANCH,
        maintainer_can_modify: false,   /* cross-fork PRs from public repos */
      }),
    });
    console.log(`    ✓ opened PR #${pr.number}: ${pr.html_url}`);
    results.prOpened++;
  } catch (e) {
    console.log(`  ✗ ${tag} → ${e.message}`);
    results.error++;
  }
}

console.log('\nSummary:');
console.log('  Up to date     :', results.fresh);
console.log('  PR opened      :', results.prOpened);
console.log('  PR already open:', results.prExists);
console.log('  Ahead (skipped):', results.ahead);
console.log('  Private        :', results.skippedPrivate);
console.log('  Errors         :', results.error);
if (!APPLY) console.log('\n(dry run — re-run with --apply to actually open PRs)');
